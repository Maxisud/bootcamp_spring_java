package org.example.sesipertama;

public class DataType {
    public static void main(String[] args) {

//        Rumus Variable: DATA_TYPE VARIABLE = NILAI;

        String iniVariable = "Ilham Zakariya";

//        TIPE DATA PRIMITIF & TIPE DATA BUKAN PRIMITIF

        // TIPE DATA PRIMITIV -> turunan dari tipe data bukan primitv

        //Float
        float iniFloat = 3.14f; //menyimpan 32 bit

        //Double
        double iniDouble = 3.14; //menyimpan 64 bit

        //Byte
        byte iniByte = 25; //menyimpan bilangan bulat, 8 bit (-127 sampai 127)

        //Short
        short iniShort = 20; //menyimpan bilangan bulat, 16 bit (-32,768 sampai 32,768)

        //Int -> integer = bilangan bulat
        int iniInt = 10000; //menyimpan bilangan bulat, 32 bit (-2.000.000.000 sampai 2.000.000)

        //Long
        long iniLong = 20000L; //menyimpan bilangan bulat, 64 bit. (>2 milyar)

        //Character
        char iniChar = 'A';

        //Boolean
        boolean iniBoolean = true;


        //TIPE DATA BUKAN PRIMITIV -> OBJEK

        String iniString = "Hello World";

        Float iniFloat2 = 3.14f;
        Double iniDouble2 = 3.14;
        Byte iniByte2 = 25;
        Short iniShort2 = 20;
        Integer iniInt2 = 10000;
        Long iniLong2 = 20000L;

        Character iniChar2 = 'A';

        Boolean iniBoolean2 = true;
    }
}
